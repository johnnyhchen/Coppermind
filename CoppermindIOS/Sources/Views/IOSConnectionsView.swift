    /// Medium widget: shows 3 top priority notes.
    struct MediumWidgetView: View {
        let notes: [(title: String, category: String?)]

        var body: some View {
            VStack(alignment: .leading, spacing: 4) {
                HStack {
                    Image(systemName: "brain")
                        .foregroundStyle(.blue)
                    Text("Coppermind")
                        .font(.headline)
                }
                ForEach(Array(notes.prefix(3).enumerated()), id: \.offset) { _, note in
                    HStack {
                        Text(note.title)
                            .font(.subheadline)
                            .lineLimit(1)
                        Spacer()
                        if let cat = note.category {
                            Text(cat)
                                .font(.caption2)
                                .foregroundStyle(.secondary)
                        }
                    }
                }
            }
            .padding()
        }
    }
}
