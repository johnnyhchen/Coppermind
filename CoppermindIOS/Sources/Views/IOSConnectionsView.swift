enum CoppermindWidgets {

    /// Small widget: shows top priority note title.
    struct SmallWidgetView: View {
        let noteTitle: String
        let category: String?

        var body: some View {
            VStack(alignment: .leading) {
                Image(systemName: "brain")
                    .foregroundStyle(.blue)
